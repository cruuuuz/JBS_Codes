def get_spot_weather_data(variable_type : str, proxies: dict, month=month, prior_month=prior_month):

    url = f'https://www.ncei.noaa.gov/data/nclimgrid-daily/access/averages/{year}/{variable_type}-{year}{month}-cty-prelim.csv'

    try:
        response = requests.get(url, proxies=proxies)
        if response.status_code != 200:
            url = f'https://www.ncei.noaa.gov/data/nclimgrid-daily/access/averages/{year}/{variable_type}-{year}{prior_month}-cty-prelim.csv'
            response = requests.get(url, proxies=proxies)
            month = prior_month
        csv_data = StringIO(response.text)
        df = pandas.read_csv(csv_data, header=None)
    except:
        print('Deu errado')


    df_counties = df[[1,2]]
    df = df.drop(columns=[0,1])

    month = int(month)
    dias = []
    primeiro_dia = datetime.datetime(year, month, 1)
    ultimo_dia = calendar.monthrange(year, month)[1]
    ultimo_dia = datetime.datetime(year, month, ultimo_dia)

    for c in range(primeiro_dia.day, ultimo_dia.day+1):
        c = datetime.datetime(year, month, c).date()
        dias.append(c)
        c + datetime.timedelta(days=1)

    print(f'{year}{month} - {ultimo_dia - primeiro_dia}')

    if len(df.columns) > len(dias):
        df = df.iloc[:, :len(dias)+4]

    for x in range(0, len(df.columns)-4):
        df.rename(columns={df.columns[x+4] : f'{dias[x]}'}, inplace=True)
        x += 1

    df.rename(columns={2 : 'State'}, inplace=True)
    df.rename(columns={3 : 'Year'}, inplace=True)
    df.rename(columns={5 : 'Weather_Variable'}, inplace=True)
    df.rename(columns={4 : 'Month'}, inplace=True)
    variable_type = df['Weather_Variable'].iloc[0]
    df = df.drop(columns=['Month', 'Weather_Variable', 'Year'])


    df = df.T
    df.reset_index(inplace=True)
    df.columns = df.iloc[0]
    df = df[1:]
    df = pandas.melt(df, id_vars=['State'], value_name='Value')
    df = df[df['Value'] != -999.99]

    df_final = df
    new_columns = df_final[0].str.split(': ', expand=True)
    df = df_final
    df = pandas.concat([new_columns,df_final], axis=1)
    if variable_type == 'TAVG':
        df['VARIABLE_TYPE'] = 'Temperature_Avg'
    elif variable_type == 'TMIN':
        df['VARIABLE_TYPE'] = 'Temperature_Min'
    elif variable_type == 'TMAX':
        df['VARIABLE_TYPE'] = 'Temperature_Max'
    else:
        df['VARIABLE_TYPE'] = 'Precipitation'

    
    df.columns = ['STATE', 'COUNTY', 'DATE', 0, 'VALUE', 'VARIABLE_TYPE']
    df = df.drop(columns=0)
    df = df[['DATE', 'STATE', 'COUNTY', 'VARIABLE_TYPE', 'VALUE']]


    #Parse County Data
    df_counties.columns = ['Code', 'State: County']
    df_counties['County_Code'] = df_counties['Code'].astype(str).str[-3:]
    df_counties['State_Code'] = df_counties['Code'].astype(str).str[:-3]
    df_counties['State_Acronimo'] = df_counties['State: County'].str[:2]
    df_counties['State'] = df_counties['State_Acronimo'].map(state_right_name)
    df_counties['State_Right_Code'] = df_counties['State'].map(state_right_code)
    df_counties['Final_Code'] = df_counties['State_Right_Code'] + df_counties['County_Code']
    df_counties = df_counties[['State: County', 'Final_Code']]
    df_counties = df_counties.drop_duplicates()
    df_dict = df_counties.set_index('State: County').to_dict(orient='dict')['Final_Code']
    
    df = df[['DATE', 'STATE',  'COUNTY', 'VARIABLE_TYPE', 'VALUE']]
    df['TMP_KEY'] = df['STATE'] + ": " + df['COUNTY']
    df['TMP_KEY'] = df['TMP_KEY'].map(df_dict)
    df['DISTRICT'] = df['TMP_KEY'].map(key_code_district)
    df = df[['DATE', 'STATE', 'DISTRICT', 'COUNTY', 'VARIABLE_TYPE', 'VALUE']]
        
    return df


