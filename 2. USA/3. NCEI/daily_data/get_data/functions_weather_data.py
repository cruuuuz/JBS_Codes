def parse_data(df, df_counties):
    df_counties_total = df_counties
    df_counties_total.columns = ['State: County', 'Code']
    df_counties_total = df_counties_total[[ 'Code', 'State: County']]
    df_counties_total['County_Code'] = df_counties_total['Code'].astype(str).str[-3:]
    df_counties_total['State_Code'] = df_counties_total['Code'].astype(str).str[:-3]
    df_counties_total['State_Acronimo'] = df_counties_total['State: County'].str[:2]
    df_counties_total['State'] = df_counties_total['State_Acronimo'].map(state_right_name)
    df_counties_total['State_Right_Code'] = df_counties_total['State'].map(state_right_code)
    df_counties_total['Final_Code'] = df_counties_total['State_Right_Code'] + df_counties_total['County_Code']
    df_counties_total = df_counties_total[['State: County', 'Final_Code']]
    df_counties_total = df_counties_total.drop_duplicates()
    df_dict = df_counties_total.set_index('State: County').to_dict(orient='dict')['Final_Code']

    df_FinalWeatherVariable = df
    df_FinalWeatherVariable = df_FinalWeatherVariable[['DATE', 'STATE',  'COUNTY', 'VARIABLE_TYPE', 'VALUE']]
    df_FinalWeatherVariable['TMP_KEY'] = df_FinalWeatherVariable['STATE'] + ": " + df_FinalWeatherVariable['COUNTY']
    df_FinalWeatherVariable['TMP_KEY'] = df_FinalWeatherVariable['TMP_KEY'].map(df_dict)
    df_FinalWeatherVariable['DISTRICT'] = df_FinalWeatherVariable['TMP_KEY'].map(key_code_district)
    df_FinalWeatherVariable['DISTRICT'] = pandas.to_numeric(df_FinalWeatherVariable['DISTRICT'])
    df_FinalWeatherVariable = df_FinalWeatherVariable[['DATE', 'STATE', 'DISTRICT', 'COUNTY', 'VARIABLE_TYPE', 'VALUE']]
    df_FinalWeatherVariable = df_FinalWeatherVariable[df_FinalWeatherVariable['VALUE'] != -999.99]
    return df_FinalWeatherVariable


